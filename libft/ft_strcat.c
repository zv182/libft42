char	*ft_strcat(char *dst, char *src)
{
	int i;

	i = 0;
	while (dst[i] != '\0')
		i++;
	while (*src != '\0')
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (dst);
}
