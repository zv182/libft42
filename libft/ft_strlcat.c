#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	sl;
	size_t	dl;

	dl = ft_strlen(dst);
	sl = ft_strlen(src);
	i = 0;
	if (size <= dl)
		return (sl + size);
	while (dst[i] != '\0' && i < size - 1)
		i++;
	while (i < size - 1 && *src)
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (dl + sl);
}
